//conditional statements
let time = 10;
let greetings;

if (time < 10) {
  greetings = "Good Morning";
  console.log(greetings);
} else if (time < 20) {
  greetings = "Good Day";
  console.log(greetings);
} else {
  greetings = "Good Evening";
  console.log(greetings);
}
